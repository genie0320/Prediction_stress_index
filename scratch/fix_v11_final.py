    "            'alpha': trial.suggest_float('alpha', 1e-3, 10.0, log=True),\n",
    "            'lambda': trial.suggest_float('lambda', 1e-3, 10.0, log=True),\n",
    "        }\n",
    "\n",
    "        kf = KFold(n_splits=5, shuffle=True, random_state=42)\n",
    "        mae_scores = []\n",
    "\n",
    "        for train_idx, val_idx in kf.split(x_train):\n",
    "            X_tr, y_tr = x_train.iloc[train_idx], y_train_logit.iloc[train_idx]\n",
    "            X_val = x_train.iloc[val_idx]\n",
    "            y_val_orig = y_train.iloc[val_idx]\n",
