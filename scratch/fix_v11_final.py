# Cell 8: Seed Averaging - XGBoost + LightGBM 앙상블 + Quantile Rounding
# =========================================================
nb['cells'][8]['source'] = [
    "# ==========================================\n",
    "# 7. Seed Averaging + Ensemble + Quantile Rounding\n",
    "# ==========================================\n",
    "\n",
    "def train_xgb_with_seeds(x_train, y_train, x_test, best_params, seeds=[42, 2026, 777]):\n",
    "    y_train_logit = logit(np.clip(y_train, epsilon, 1 - epsilon))\n",
    "    oof_preds = np.zeros(len(x_train))\n",
    "    test_preds = np.zeros(len(x_test))\n",
    "    kf = KFold(n_splits=5, shuffle=True, random_state=42)\n",
    "\n",
    "    for fold, (train_idx, val_idx) in enumerate(kf.split(x_train)):\n",
    "        X_tr, y_tr = x_train.iloc[train_idx], y_train_logit.iloc[train_idx]\n",
    "        X_val = x_train.iloc[val_idx]\n",
    "        fold_val_preds = np.zeros(len(X_val))\n",
    "        fold_test_preds = np.zeros(len(x_test))\n",
    "\n",
    "        for seed in seeds:\n",
    "            params = best_params.copy()\n",
    "            params.update({\n",
    "                'objective': 'reg:absoluteerror',  # [CRITICAL FIX]\n",
    "                'random_state': seed,\n",
    "                'verbosity': 0,\n",
    "                'enable_categorical': True,\n",
    "                'tree_method': 'hist',\n",
    "                'n_jobs': 1,\n",
    "            })\n",
    "\n",
    "            model = xgb.XGBRegressor(**params)\n",
    "            model.fit(X_tr, y_tr, eval_set=[(X_val, y_train_logit.iloc[val_idx])], verbose=False)\n",
    "\n",
    "            fold_val_preds += np.clip(expit(model.predict(X_val)), 0.0, 1.0) / len(seeds)\n",
    "            fold_test_preds += np.clip(expit(model.predict(x_test)), 0.0, 1.0) / len(seeds)\n",
    "\n",
    "        oof_preds[val_idx] = fold_val_preds\n",
    "        test_preds += fold_test_preds / 5\n",
    "\n",
    "    mae = mean_absolute_error(y_train, oof_preds)\n",
    "    print(f'[XGBoost] Seed Averaged OOF MAE: {mae:.6f}')\n",
    "    return oof_preds, test_preds\n",
    "\n",
    "\n",
    "def train_lgb_with_seeds(x_train, y_train, x_test, best_params, seeds=[42, 2026, 777]):\n",
    "    y_train_logit = logit(np.clip(y_train, epsilon, 1 - epsilon))\n",
    "    oof_preds = np.zeros(len(x_train))\n",
    "    test_preds = np.zeros(len(x_test))\n",
    "    kf = KFold(n_splits=5, shuffle=True, random_state=42)\n",
    "\n",
    "    for fold, (train_idx, val_idx) in enumerate(kf.split(x_train)):\n",
    "        X_tr, y_tr = x_train.iloc[train_idx], y_train_logit.iloc[train_idx]\n",
    "        X_val = x_train.iloc[val_idx]\n",
    "        fold_val_preds = np.zeros(len(X_val))\n",
    "        fold_test_preds = np.zeros(len(x_test))\n",
    "\n",
    "        for seed in seeds:\n",
    "            params = best_params.copy()\n",
    "            params.update({\n",
    "                'objective': 'regression_l1',\n",
    "                'random_state': seed,\n",
    "                'verbose': -1,\n",
    "                'n_jobs': 1,\n",
    "            })\n",
    "\n",
    "            model = lgb.LGBMRegressor(**params)\n",
    "            model.fit(X_tr, y_tr, eval_set=[(X_val, y_train_logit.iloc[val_idx])],\n",
    "                      callbacks=[lgb.early_stopping(50, verbose=False), lgb.log_evaluation(-1)])\n",
    "\n",
    "            fold_val_preds += np.clip(expit(model.predict(X_val)), 0.0, 1.0) / len(seeds)\n",
    "            fold_test_preds += np.clip(expit(model.predict(x_test)), 0.0, 1.0) / len(seeds)\n",
    "\n",
    "        oof_preds[val_idx] = fold_val_preds\n",
    "        test_preds += fold_test_preds / 5\n",
    "\n",
    "    mae = mean_absolute_error(y_train, oof_preds)\n",
    "    print(f'[LightGBM] Seed Averaged OOF MAE: {mae:.6f}')\n",
    "    return oof_preds, test_preds\n",
    "\n",
    "\n",
    "# ---- 앙상블 실행 ----\n",
    "print('=== Training XGBoost (3 seeds x 5 folds) ===')\n",
    "oof_xgb, test_xgb = train_xgb_with_seeds(x_train, y_train, x_test, best_xgb)\n",
    "\n",
    "print('=== Training LightGBM (3 seeds x 5 folds) ===')\n",
    "oof_lgb, test_lgb = train_lgb_with_seeds(x_train, y_train, x_test, best_lgb)\n",
    "\n",
    "# ---- 가중 평균 앙상블 (OOF로 최적 가중치 탐색) ----\n",
    "best_weight = 0.5\n",
    "best_oof_mae = float('inf')\n",
    "for w in np.arange(0.3, 0.8, 0.05):\n",
    "    oof_blend = w * oof_xgb + (1 - w) * oof_lgb\n",
    "    oof_blend = np.clip(oof_blend, 0.0, 1.0)\n",
    "    mae = mean_absolute_error(y_train, oof_blend)\n",
    "    if mae < best_oof_mae:\n",
    "        best_oof_mae = mae\n",
    "        best_weight = w\n",
    "\n",
    "print(f'\\nBest blend weight (XGBoost): {best_weight:.2f}')\n",
    "print(f'Blended OOF MAE: {best_oof_mae:.6f}')\n",
    "\n",
    "final_oof = np.clip(best_weight * oof_xgb + (1 - best_weight) * oof_lgb, 0.0, 1.0)\n",
    "test_preds_raw = np.clip(best_weight * test_xgb + (1 - best_weight) * test_lgb, 0.0, 1.0)\n",
    "\n",
    "# ---- [BREAKTHROUGH] Quantile Rounding ----\n",
    "# 타겟은 0.01 단위(101개 클래스)로 이산화되어 있음\n",
    "# 예측값도 0.01 단위로 반올림하면 MAE가 소폭 개선될 수 있음\n",
    "test_preds_rounded = np.round(test_preds_raw * 100) / 100\n",
    "oof_rounded = np.round(final_oof * 100) / 100\n",
    "\n",
    "mae_raw = mean_absolute_error(y_train, final_oof)\n",
    "mae_rounded = mean_absolute_error(y_train, oof_rounded)\n",
    "print(f'\\nOOF MAE (raw):    {mae_raw:.6f}')\n",
    "print(f'OOF MAE (rounded): {mae_rounded:.6f}')\n",
    "\n",
    "# 반올림이 도움되는 경우만 채택\n",
    "if mae_rounded < mae_raw:\n",
    "    test_preds = test_preds_rounded\n",
    "    print('→ Rounding HELPS: using rounded predictions')\n",
    "else:\n",
    "    test_preds = test_preds_raw\n",
    "    print('→ Rounding does not help: using raw predictions')\n",
    "\n",
    "print(f'\\nFinal OOF MAE: {min(mae_raw, mae_rounded):.6f}')\n",
    "print(f'Prediction std: {test_preds.std():.4f} (target std: {y_train.std():.4f})')\n",
]

